/**
 * String data type
 * - string
 *
 * * Function For String
 * - len("string"): count the number of chars (6)
 * - "string"[number]: get a char in byte from the string ("string"[2] => 82)
 */

package main

import "fmt"

func main() {
	fmt.Println("Rico")
	fmt.Println(len("Rico"))
	fmt.Println("Rico"[2])
}
